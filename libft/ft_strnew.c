#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	c;

	c = size + 1;
	if (c < size)
		return (NULL);
	str = (char *)malloc(sizeof(char) * size + 1);
	if (!str)
		return (NULL);
	ft_bzero(str, size + 1);
	return (str);
}
